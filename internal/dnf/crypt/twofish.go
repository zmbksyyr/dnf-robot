func calcS(a, b, c, d byte, key []byte, i int, w, x, y, z byte) (byte, byte, byte, byte) {
	if key[i] != 0 {
		tmp := polyToExp[key[i]-1]
		a ^= expToPoly[int(tmp)+int(w)]
		b ^= expToPoly[int(tmp)+int(x)]
		c ^= expToPoly[int(tmp)+int(y)]
		d ^= expToPoly[int(tmp)+int(z)]
	}
	return a, b, c, d
}

func calcK2(ctx *TwoFishCipher, a, b, c, d byte, j int, key []byte) uint32 {
	return mdsTable[0][q0[a^key[j+8]]^key[j]] ^
		mdsTable[1][q0[b^key[j+9]]^key[j+1]] ^
		mdsTable[2][q1[c^key[j+10]]^key[j+2]] ^
		mdsTable[3][q1[d^key[j+11]]^key[j+3]]
}

func calcK(ctx *TwoFishCipher, dst []uint32, j int, k, l, m, n byte, key []byte) {
	x := calcK2(ctx, k, l, k, l, 0, key)
	y := calcK2(ctx, m, n, m, n, 4, key)
	y = (y << 8) | (y >> 24)
	x += y
	y += x
	dst[j] = x
	dst[j+1] = (y << 9) | (y >> 23)
}

func calcK1922(ctx *TwoFishCipher, a, b, c, d byte, j int, key []byte) uint32 {
	return calcK2(ctx,
		q0[a^key[j+16]],
		q1[b^key[j+17]],
		q0[c^key[j+18]],
		q1[d^key[j+19]],
		j, key)
}

