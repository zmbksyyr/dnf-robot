package crypt

import "encoding/binary"

type TwoFishCipher struct {
	s    [4][256]uint32
	w    [8]uint32
	k    [32]uint32
	init bool
}

func NewTwoFishCipher() *TwoFishCipher {
	return &TwoFishCipher{}
}

func (t *TwoFishCipher) SetKey(key []byte) error {
	if len(key) != 16 && len(key) != 24 && len(key) != 32 {
		return ErrInvalidKeySize
	}
	t.init = true
	twofishSetKey(t, key, len(key))
	return nil
}

func (t *TwoFishCipher) KeySize() int {
	return 32
}

func (t *TwoFishCipher) BlockSize() int {
	return 16
}

func (t *TwoFishCipher) Encrypt(data []byte) ([]byte, error) {
	if !t.init {
		return nil, ErrNotInitialized
	}
	blockSize := t.BlockSize()
	if len(data)%blockSize != 0 {
		return nil, ErrInvalidBlockSize
	}
	out := make([]byte, len(data))
	for i := 0; i < len(data); i += blockSize {
		t.encryptBlock(data[i:i+blockSize], out[i:i+blockSize])
	}
	return out, nil
}

func (t *TwoFishCipher) Decrypt(data []byte) ([]byte, error) {
	if !t.init {
		return nil, ErrNotInitialized
	}
	blockSize := t.BlockSize()
	if len(data)%blockSize != 0 {
		return nil, ErrInvalidBlockSize
	}
	out := make([]byte, len(data))
	for i := 0; i < len(data); i += blockSize {
		t.decryptBlock(data[i:i+blockSize], out[i:i+blockSize])
	}
	return out, nil
}

func (t *TwoFishCipher) encryptBlock(in, out []byte) {
	a := binary.LittleEndian.Uint32(in[0:4]) ^ t.w[0]
	b := binary.LittleEndian.Uint32(in[4:8]) ^ t.w[1]
	c := binary.LittleEndian.Uint32(in[8:12]) ^ t.w[2]
	d := binary.LittleEndian.Uint32(in[12:16]) ^ t.w[3]

	var x, y uint32
	for i := 0; i < 16; i += 2 {
		x = g1(t, a)
		y = g2(t, b)
		x += y
		y += x + t.k[2*i+1]
		c ^= x + t.k[2*i]
		c = (c >> 1) | (c << 31)
		d = ((d << 1) | (d >> 31)) ^ y

		x = g1(t, c)
		y = g2(t, d)
		x += y
		y += x + t.k[2*i+3]
		a ^= x + t.k[2*i+2]
		a = (a >> 1) | (a << 31)
		b = ((b << 1) | (b >> 31)) ^ y
	}
	c ^= t.w[4]
	d ^= t.w[5]
	a ^= t.w[6]
	b ^= t.w[7]
	binary.LittleEndian.PutUint32(out[0:4], c)
	binary.LittleEndian.PutUint32(out[4:8], d)
	binary.LittleEndian.PutUint32(out[8:12], a)
	binary.LittleEndian.PutUint32(out[12:16], b)
}

func (t *TwoFishCipher) decryptBlock(in, out []byte) {
	c := binary.LittleEndian.Uint32(in[0:4]) ^ t.w[4]
	d := binary.LittleEndian.Uint32(in[4:8]) ^ t.w[5]
	a := binary.LittleEndian.Uint32(in[8:12]) ^ t.w[6]
	b := binary.LittleEndian.Uint32(in[12:16]) ^ t.w[7]

	var x, y uint32
	for i := 15; i >= 0; i -= 2 {
		x = g1(t, c)
		y = g2(t, d)
		x += y
		y += x
		b ^= y + t.k[2*i+1]
		b = (b >> 1) | (b << 31)
		a = ((a << 1) | (a >> 31))
		a ^= x + t.k[2*i]

		x = g1(t, a)
		y = g2(t, b)
		x += y
		y += x
		d ^= y + t.k[2*i-1]
		d = (d >> 1) | (d << 31)
		c = ((c << 1) | (c >> 31))
		c ^= x + t.k[2*i-2]
	}
	a ^= t.w[0]
	b ^= t.w[1]
	c ^= t.w[2]
	d ^= t.w[3]
	binary.LittleEndian.PutUint32(out[0:4], a)
	binary.LittleEndian.PutUint32(out[4:8], b)
	binary.LittleEndian.PutUint32(out[8:12], c)
	binary.LittleEndian.PutUint32(out[12:16], d)
}

func g1(ctx *TwoFishCipher, a uint32) uint32 {
	return ctx.s[0][a&0xFF] ^ ctx.s[1][(a>>8)&0xFF] ^ ctx.s[2][(a>>16)&0xFF] ^ ctx.s[3][a>>24]
}

func g2(ctx *TwoFishCipher, b uint32) uint32 {
	return ctx.s[1][b&0xFF] ^ ctx.s[2][(b>>8)&0xFF] ^ ctx.s[3][(b>>16)&0xFF] ^ ctx.s[0][b>>24]
}

