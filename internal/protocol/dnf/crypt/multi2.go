func NewMulti2Cipher() *Multi2Cipher {
	return &Multi2Cipher{}
}

func (m *Multi2Cipher) SetKey(key []byte) error {
	if len(key) < 8 {
		return ErrInvalidKeySize
	}
	m.N = len(key) / 4
	if m.N > 8 {
		m.N = 8
	}
	for i := 0; i < m.N; i++ {
		m.uk[i] = loadU32BE(key[4*i : 4*i+4])
	}
	return nil
}

