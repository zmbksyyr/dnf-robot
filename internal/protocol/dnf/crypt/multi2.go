func multi2G(in uint16) uint16 {
	return uint16(multi2Pi1[in>>8])<<8 | uint16(multi2Pi1[byte(in)])
}

func multi2Ginv(in uint16) uint16 {
	return uint16(multi2Pi3[in>>8])<<8 | uint16(multi2Pi3[byte(in)])
}

func multi2H(in uint16) uint16 {
	return uint16(multi2Pi2[in>>8])<<8 | uint16(multi2Pi2[byte(in)])
}

func multi2Hinv(in uint16) uint16 {
	return uint16(multi2Pi4[in>>8])<<8 | uint16(multi2Pi4[byte(in)])
}

func (m *Multi2Cipher) Encrypt(data []byte) ([]byte, error) {
	blockSize := m.BlockSize()
	if len(data)%blockSize != 0 {
		return nil, ErrInvalidBlockSize
	}
	out := make([]byte, len(data))
	tmp := make([]byte, 8)
	for i := 0; i < len(data); i += blockSize {
		copy(tmp, data[i:i+8])
		for j := 0; j < m.N; j++ {
			tmp[0] ^= byte(m.uk[j] >> 24)
			tmp[1] ^= byte(m.uk[j] >> 16)
			tmp[2] ^= byte(m.uk[j] >> 8)
			tmp[3] ^= byte(m.uk[j])
			d1 := uint16(tmp[0])<<8 | uint16(tmp[1])
			d2 := uint16(tmp[2])<<8 | uint16(tmp[3])
			d3 := uint16(tmp[4])<<8 | uint16(tmp[5])
			d4 := uint16(tmp[6])<<8 | uint16(tmp[7])
			d1 = multi2G(d1)
			d2 = multi2Ginv(d2)
			d3 = multi2H(d3)
			d4 = multi2Hinv(d4)
			tmp[0] = byte(d1 >> 8)
			tmp[1] = byte(d1)
			tmp[2] = byte(d2 >> 8)
			tmp[3] = byte(d2)
			tmp[4] = byte(d3 >> 8)
			tmp[5] = byte(d3)
			tmp[6] = byte(d4 >> 8)
			tmp[7] = byte(d4)
		}
		copy(out[i:i+8], tmp)
	}
	return out, nil
}

func (m *Multi2Cipher) Decrypt(data []byte) ([]byte, error) {
	blockSize := m.BlockSize()
	if len(data)%blockSize != 0 {
		return nil, ErrInvalidBlockSize
	}
	out := make([]byte, len(data))
	tmp := make([]byte, 8)
	for i := 0; i < len(data); i += blockSize {
		copy(tmp, data[i:i+8])
		for j := m.N - 1; j >= 0; j-- {
			d1 := uint16(tmp[0])<<8 | uint16(tmp[1])
			d2 := uint16(tmp[2])<<8 | uint16(tmp[3])
			d3 := uint16(tmp[4])<<8 | uint16(tmp[5])
			d4 := uint16(tmp[6])<<8 | uint16(tmp[7])
			d1 = multi2Hinv(d1)
			d2 = multi2H(d2)
			d3 = multi2Ginv(d3)
			d4 = multi2G(d4)
			tmp[0] = byte(d1 >> 8)
			tmp[1] = byte(d1)
			tmp[2] = byte(d2 >> 8)
			tmp[3] = byte(d2)
			tmp[4] = byte(d3 >> 8)
			tmp[5] = byte(d3)
			tmp[6] = byte(d4 >> 8)
			tmp[7] = byte(d4)
			tmp[0] ^= byte(m.uk[j] >> 24)
			tmp[1] ^= byte(m.uk[j] >> 16)
			tmp[2] ^= byte(m.uk[j] >> 8)
			tmp[3] ^= byte(m.uk[j])
		}
		copy(out[i:i+8], tmp)
	}
	return out, nil
}
